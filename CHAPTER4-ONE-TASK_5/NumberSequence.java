public class NumberSequence {
    public static void main(String[] args) {
        // Menggunakan for loop untuk menghasilkan urutan nombor
        for (int i = 99; i >= 11; i -= 11) { //Menggunakan "for" loop dengan i bermula dari 99 dan dikurangkan secara langsung sebanyak 11 setiap kali.
            System.out.print(i + " "); // Cetak nombor dengan ruang di antara
        }
    }
}
