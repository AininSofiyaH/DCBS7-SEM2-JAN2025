package Chapter4;

public class task1 {public static void main(String[]args) {
	System.out.println("List of number 1 to 10 using for loop:");
	for(int i = 1;i <= 10; i++) { // using for loop to print 1 to 10
		System.out.println(i);
	}
	System.out.println("\nList of number 1 to 10 using while loop:");
	int i = 1;
	while (i <= 10) { //using while loop to print 1 to 10
		System.out.println(i++);
		
	}
}


}
