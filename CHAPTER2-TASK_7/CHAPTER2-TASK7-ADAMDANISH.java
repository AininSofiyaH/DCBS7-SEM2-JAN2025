public class CompoundProg {
    public static void main(String[] args) {
       System.out.println("Pre-Increment [++VAR]");
       System.out.println("Incremented first, and then the new value is used in the expression");
       System.out.println("before increment = 4");
       System.out.println("during increment = 5");
       System.out.println("after increment = 5");
       System.out.println("         ");
       
       System.out.println("Post-Increment [VAR++]");
       System.out.println("Value is used first, and then its is incremented");
       System.out.println("before increment = 5");
       System.out.println("during increment = 5");
       System.out.println("after increment = 6");
       System.out.println("          ");
       
       
       System.out.println("Pre-Decrement [--VAR]");
       System.out.println("Decremented first, and then the updated value is used in the expression");
       System.out.println("before decrement = 5");
       System.out.println("during decrement = 4");
       System.out.println("after decrement = 4");
       System.out.println("          ");
       
       
       System.out.println("Post-Decrement [VAR--]");
       System.out.println("Current value is used in the expression first, and then it is decremented");
       System.out.println("before decrement = 5");
       System.out.println("during decrement = 5");
       System.out.println("after decrement = 4");
           

   }
}
