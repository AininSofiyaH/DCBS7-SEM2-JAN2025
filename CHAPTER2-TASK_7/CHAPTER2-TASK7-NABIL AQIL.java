public class CompoundProg {
    public static void main(String[] args) {
        //The pre-increment
        System.out.println("++VARIABLES - PRE-INCREMENT");
        System.out.println("It increments, then use the value");
        System.out.println("Ex. if a = 0, ++a will print out 1");
        System.out.println("Original Number : 0");
        System.out.println("Pre-Increment : 1");
        System.out.println("New Number : 1");
        System.out.println("");
        
        //The post-increment
        System.out.println("VARIABLES++ - POST-INCREMENT");
        System.out.println("It use the value, then increments");
        System.out.println("Ex. if a = 0, a++ will print out 0 but the value has been set to 1");
        System.out.println("Original Number : 0");
        System.out.println("Pre-Increment : 0");
        System.out.println("New Number : 1");
        System.out.println("");
        
        //The pre-decrement
        System.out.println("--VARIABLES - PRE-DECREMENT");
        System.out.println("It decrements, then use the value");
        System.out.println("Ex. if a = 0, ++a will print out -1");
        System.out.println("Original Number : 0");
        System.out.println("Pre-Decrement : -1");
        System.out.println("New Number : -1");
        System.out.println("");
        
        //The post-decrement
        System.out.println("VARIABLES-- - POST-DECREMENT");
        System.out.println("It use the value, then decrements");
        System.out.println("Ex. if a = 0, a++ will print out 0 but the value has been set to -1");
        System.out.println("Original Number : 0");
        System.out.println("Pre-Increment : 0");
        System.out.println("New Number : -1");
        System.out.println("");
    }
}
