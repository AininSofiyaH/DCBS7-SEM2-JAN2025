public class CHAPTER2_TASK7_NURAZREEN {

    public static void main(String[] args) {
        System.out.println("INCREMENT & DECREMENT");

        System.out.println(" ");
        System.out.println(" ++ VARIABLE (PRE INCREMENT)");
        System.out.println("int e = 6 \n int z  = ++e \n output: e = 7 \n       y =7");
        
        System.out.println(" VARIABLE ++ (POST INCREMENT)");
        System.out.println("int e = 6 \n int z = e++ \n output: e = 7 \n        z = 6");
        
        System.out.println(" -- VARIABLE (PRE DECREMENT)"); 
        System.out.println("int e = 6 \n int z = --e \n output: e = 5 \n        z = 5");
        
        System.out.println(" VARIABLE -- (POST DECREMENT)");
        System.out.println("int e = 6 \n int z = e-- \n output: e= 5\n        z = 6");
        
    }
}
